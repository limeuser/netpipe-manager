package netpipe.manager;

public class Cpu {
    private int hz;  // M
    private int cores;
    public int getHz() {
        return hz;
    }
    public void setHz(int hz) {
        this.hz = hz;
    }
    public int getCores() {
        return cores;
    }
    public void setCores(int cores) {
        this.cores = cores;
    }
}
